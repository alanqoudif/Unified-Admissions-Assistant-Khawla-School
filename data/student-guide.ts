// وظيفة للبحث في محتوى دليل الطالب
export function searchGuideContent(query: string): string {
  // تنفيذ بسيط للبحث في المحتوى
  const lowerQuery = query.toLowerCase()
  const lowerContent = studentGuide.toLowerCase()

  if (lowerContent.includes(lowerQuery)) {
    // إذا وجد النص المطلوب، قم بإرجاع الفقرة التي تحتوي عليه
    const paragraphs = studentGuide.split("\n\n")
    const relevantParagraphs = paragraphs.filter((p) => p.toLowerCase().includes(lowerQuery))

    return relevantParagraphs.join("\n\n")
  }

  return "لم يتم العثور على معلومات محددة حول هذا الاستفسار في دليل الطالب."
}

// وظيفة للبحث عن معلومات حول تخصص معين
export function searchByMajor(major: string): string {
  const majorKeywords = major.toLowerCase().split(/\s+/)
  const paragraphs = studentGuide.split("\n\n")

  // البحث عن الفقرات التي تحتوي على كلمات مفتاحية متعلقة بالتخصص
  const relevantParagraphs = paragraphs.filter((p) => {
    const lowerP = p.toLowerCase()
    return majorKeywords.some((keyword) => lowerP.includes(keyword))
  })

  if (relevantParagraphs.length > 0) {
    return relevantParagraphs.join("\n\n")
  }

  return `لم يتم العثور على معلومات محددة حول تخصص "${major}" في دليل الطالب.`
}

// وظيفة للبحث عن معلومات حول مؤسسة تعليمية معينة
export function searchByInstitution(institution: string): string {
  const institutionKeywords = institution.toLowerCase().split(/\s+/)
  const paragraphs = studentGuide.split("\n\n")

  // البحث عن الفقرات التي تحتوي على كلمات مفتاحية متعلقة بالمؤسسة
  const relevantParagraphs = paragraphs.filter((p) => {
    const lowerP = p.toLowerCase()
    return institutionKeywords.some((keyword) => lowerP.includes(keyword))
  })

  if (relevantParagraphs.length > 0) {
    return relevantParagraphs.join("\n\n")
  }

  return `لم يتم العثور على معلومات محددة حول مؤسسة "${institution}" في دليل الطالب.`
}

